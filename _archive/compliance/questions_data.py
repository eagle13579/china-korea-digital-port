def get_questions(language="zh"):
    """获取题目数据（用于API响应）

    Args:
        language: "zh" 或 "ko"

    Returns:
        list: 包含题干、选项和知识引用的题目列表
    """
    result = []
    for q in QUESTIONS:
        item = {
            "id": q["id"],
            "dimension": q["dimension"],
            "dimension_label": q["dimension_label"][language],
            "question": q["question"][language],
            "options": [
                {
                    "value": opt["value"],
                    "label": opt["label"][language]
                }
                for opt in q["options"]
            ],
            "knowledge_ref": q.get("knowledge_ref", {}).get(language, ""),
            "example": q.get("example", {}).get(language, ""),
        }
        result.append(item)
    return result


def get_question_by_id(question_id):
    """根据ID查找题目定义"""
    for q in QUESTIONS:
        if q["id"] == question_id:
            return q
    return None
